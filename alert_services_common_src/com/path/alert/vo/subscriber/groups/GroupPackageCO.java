package com.path.alert.vo.subscriber.groups;

import java.math.BigDecimal;

public class GroupPackageCO {
	private BigDecimal grpId;
	private BigDecimal lineNo;
	private BigDecimal pkgId;
	
	public BigDecimal getGrpId() {
		return grpId;
	}
	public void setGrpId(BigDecimal grpId) {
		this.grpId = grpId;
	}
	public BigDecimal getLineNo() {
		return lineNo;
	}
	public void setLineNo(BigDecimal lineNo) {
		this.lineNo = lineNo;
	}
	public BigDecimal getPkgId() {
		return pkgId;
	}
	public void setPkgId(BigDecimal pkgId) {
		this.pkgId = pkgId;
	}
	
}
